#Program for executing relational operators
#RelationalOpEx1.py
a,b=float(input("Enter the value of a:")),float(input("Enter the value of b:"))
print("*"*50)
print("\tRelational operators and Expressions")
print("*"*50)
print("\t\t{} > {} = {}".format(a,b,a>b))
print("\t\t{} < {} ={}".format(a,b,a<b))
print("\t\t{} == {} ={}".format(a,b,a==b))
print("\t\t{} != {} ={}".format(a,b,a!=b))
print("\t\t{} >= {} ={}".format(a,b,a>=b))
print("\t\t{} <= {} ={}".format(a,b,a<=b))
print("*"*50)