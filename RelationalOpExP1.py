#program for demonstrating Relational operator.
#RelationalOpExP1.py
a=float(input("Enter the value of a :"))
b=float(input("Enter the value of b :"))
print("---------------------------------------")
print("Relational operators")
print("---------------------------------------")
print("\t\t{} > {} = {}".format(a,b,a>b))
print("\t\t{} < {} = {}".format(a,b,a<b))
print("\t\t{} == {} = {}".format(a,b,a==b))
print("\t\t{} != {} = {}".format(a,b,a!=b))
print("\t\t{} >= {} = {}".format(a,b,a>=b))
print("\t\t{} <= {} = {}".format(a,b,a<=b))
print("---------------------------------------")